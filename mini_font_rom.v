module mini_font_rom(
    input  wire [7:0] char_code, // ASCII 码
    input  wire [3:0] row,       // 当前扫描的字符行号 (0~15)
    output wire [7:0] row_data   // 该行的 8 个像素点阵数据 (高位在左)
);

    reg [127:0] font_data;

    // 将 16 行(每行 8-bit)拼接成 128-bit 数据，Row 0 位于最高位
    always @(*) begin
        case(char_code)
            8'h20: font_data = 128'h00_00_00_00_00_00_00_00_00_00_00_00_00_00_00_00; // 空格
            8'h25: font_data = 128'h00_00_00_00_C6_CC_18_30_60_CC_C6_00_00_00_00_00; // %
            8'h2F: font_data = 128'h00_00_00_00_02_04_08_10_20_40_80_00_00_00_00_00; // /
            8'h30: font_data = 128'h00_00_00_18_24_42_42_42_42_42_42_24_18_00_00_00; // 0
            8'h31: font_data = 128'h00_00_00_08_18_28_08_08_08_08_08_08_3E_00_00_00; // 1
            8'h32: font_data = 128'h00_00_00_18_24_42_02_04_08_10_20_42_7E_00_00_00; // 2
            8'h33: font_data = 128'h00_00_00_18_24_42_02_0C_02_42_42_24_18_00_00_00; // 3
            8'h34: font_data = 128'h00_00_00_04_0C_14_24_44_7E_04_04_04_04_00_00_00; // 4
            8'h35: font_data = 128'h00_00_00_7E_40_40_78_04_02_42_42_24_18_00_00_00; // 5
            8'h36: font_data = 128'h00_00_00_18_24_40_40_78_44_42_42_24_18_00_00_00; // 6
            8'h37: font_data = 128'h00_00_00_7E_02_04_08_10_10_10_10_10_10_00_00_00; // 7
            8'h38: font_data = 128'h00_00_00_18_24_42_42_18_42_42_42_24_18_00_00_00; // 8
            8'h39: font_data = 128'h00_00_00_18_24_42_42_42_3E_02_02_24_18_00_00_00; // 9
            8'h41: font_data = 128'h00_00_00_18_24_42_42_42_7E_42_42_42_42_00_00_00; // A
            8'h42: font_data = 128'h00_00_00_78_44_42_42_78_42_42_42_44_78_00_00_00; // B
            8'h43: font_data = 128'h00_00_00_18_24_42_40_40_40_40_42_24_18_00_00_00; // C
            8'h47: font_data = 128'h00_00_00_18_24_40_40_40_4E_42_42_24_18_00_00_00; // G
            8'h48: font_data = 128'h00_00_00_42_42_42_42_7E_42_42_42_42_42_00_00_00; // H
            8'h4D: font_data = 128'h00_00_00_42_66_5A_42_42_42_42_42_42_42_00_00_00; // M
            8'h50: font_data = 128'h00_00_00_78_44_42_42_44_78_40_40_40_40_00_00_00; // P
            8'h52: font_data = 128'h00_00_00_78_44_42_42_44_78_48_44_42_42_00_00_00; // R
            8'h54: font_data = 128'h00_00_00_7E_10_10_10_10_10_10_10_10_10_00_00_00; // T
            8'h55: font_data = 128'h00_00_00_42_42_42_42_42_42_42_42_42_24_18_00_00; // U
            8'h7A: font_data = 128'h00_00_00_00_00_00_00_7E_04_08_10_20_7E_00_00_00; // z
            default: font_data = 128'h00; // 默认空白
        endcase
    end

    // 截取对应行的数据输出。Row 0 在最高位，Row 15 在最低位。
    assign row_data = font_data[ (15 - row) * 8 +: 8 ];

endmodule